        <h2>
            Details on 1950s/60s stratospheric aerosol measurements
        </h2>
        <p>
            Measuring in-situ the stratospheric composition became an urgent priority in the 1950s to monitor the progression of radionuclide concentrations from thermonuclear tests in the Pacific and in mid-latitudes (e.g. Telegadas and List, 1964). Existing US Air Force and Atomic Energy Commission high-altitude balloon-borne and U-2 aircraft monitoring capabilities began to measure aerosol in 1960 (e.g. Friend, 1961), and the program continued to 1966 (Feely et al., 1967).
        </p>
        <p>
            Christian Junge and co-workers analyzed stratospheric aerosol measurements from high-altitude balloon (Junge et al., 1961) and aircraft (Junge and Manson, 1961), establishing the basis of today’s understanding of the vertical distribution of stratospheric aerosols (e.g. Junge, 1961; Friend, 1966).
        </p>
        <p>
            Junge et al. (1961) analyzed balloon flights made in 1958 and 1959, measuring increasing concentrations of particles larger than 100nm, up to a maximum at 20km, the layer subsequently becoming known as the Junge layer. In contrast, smaller Aitken particles, measured by expansion- type nuclei counters decreased above the tropopause, up to 25km. These measurements combined existing aerosol measurement technologies (e.g. Junge, 1935) with new sampling methods (e.g. Chagnon, 1957) for stratospheric conditions.
        </p>
        <p>
            The initial aerosol samples from U-2 flights (Mar-Nov 1960) spanned 63°S to 72°N (Junge and Manson, 1961), and established the dominant sulfate aerosol composition, with also trace metals including magnesium, silicon and iron with lower concentrations of calcium and potassium.
        </p>
        <p>
            The high-altitude sampling program of aircraft and balloon measurements continued through much of the 1960s (Feely et al., 1967). Instruments on the U-2 flights measured the volcanic aerosol cloud from the 1963 Agung eruption, including the morphology of volcanic aerosols (Mossop et al., 1963; 1964; 1965). Balloon-borne particle counters developed at the University of Minnesota (Rosen, 1964), together with a 4-wavelength solar extinction instrument developed by Ted Pepin (Pepin, 1970) were used to measure the full vertical extent of the volcanic aerosol concentrations, and provide information on the temporal variation of the stratospheric aerosol layer in mid-latitudes.
        </p>
        <p>
            The eruptions of Taal, Philippines in September 1965, Awu, Indonesia in August 1966, and Fernandina, the Galapagos Islands in June 1968 were each explosive enough to inject aerosol material into the stratosphere (see Newhall and Self, 1982). The Mauna Loa transmissions record (e.g. Mendonca et al., 1978) shows how the volcanic dimming from Agung was prolonged through to the late 1960s. In addition to the Minneapolis balloon measurements, a field campaign in Panama in September 1966 measured the Awu aerosol cloud using ballon-borne OPC instruments (Rosen, 1968) and solar extinction measurements (Pepin, 1970).
        </p>
        <p>
            Several observational synthesis papers at the end of the 1960s brought together wide range of different data sets. For example Rosen (1969) analysed both in-situ and active remote sensing measurements to understand aerosol formation processes, and Dyer and Hicks (1968) present a comprehensive synthesis of ground-based radiation measurements, then leading to our current understanding of the dispersion of the Agung aerosol cloud (Dyer, 1970).
        </p>
        <p>
            With the development of active ground-based remote sensing techniques in the early 1960's, the Agung plume was also measured by searchlight (Elterman, 1964) and lidar (Clemesha et al., 1966; Grams and Fiocco, 1967). Together with information documented in reports that were recovered from that time (e.g. Elterman, 1966; Feely et al., 1967) and PhD theses (e.g. Grams, 1966), these measurements provide information of the Northern Hemisphere altitude progression of the major volcanic aerosol cloud.
        </p>
        <p>
            Further advances in particle counter technology in the 1970s (Kaselau 1974; Haberl, 1975; Rosen and Hofmann, 1977) led new knowledge of the existence of the Aitken-sized stratospheric aerosol (Rosen et al., 1975; Cadle & Kiang, 1977; Cadle et al., 1977). Together with the long term record of in-situ aerosol measurements from Wyoming (Hofmann et al., 1975) which span the 1974 Fuego period (e.g. Hofmann and Rosen, 1977), our understanding of volcanic effects progressed further.
        </p>
