<h1>Analysis Activities</h1>
<h2>Stratospheric Sulfur Budget</h2>
<p>
    A paper on the stratospheric sulfur burden is in process to survey and compile measurements of stratospheric sulfur
    from instruments sensitive to gas phase sulfur (OCS and SO2) and particle phase sulfur. Instruments on satellites,
    aircraft, balloons, and the ground will be included. The goal is to present a temporal and spatial estimate of
    stratospheric sulfur based on the measurements available. The effort is being led by Terry Deshler.
</p>

<h2>Global Space-based Stratospheric Aerosol Climatology</h2>
<p>
    SSiRC has played a key role in the development of the Global Space-based Stratospheric Aerosol Climatology, or
    GloSSAC. GloSSAC is a 38-year record of stratospheric aerosol optical properties measured primarily by space-based
    instruments. It was developed as a part of the stratospheric aerosol forcing for the Climate Model Intercomparison
    Project (CMIP6) set (1979 to 2014) and subsequently extended through 2016. GloSSAC focuses on the Stratospheric
    Aerosol and Gas Experiment (SAGE) series of instruments through mid-2005 and on the Optical Spectrograph and
    InfraRed Imager System (OSIRIS) and the Cloud-Aerosol Lidar and Infrared Pathfinder Satellite Observation (CALIPSO)
    data thereafter. We also utilize data from other space instruments and from ground-based, air and balloon borne
    instruments to fill in key gaps in the data set. The end result is a global and gap-free data set focused on aerosol
    extinction coefficient at 525 and 1020 nm and other parameters on an 'as available' basis. We developed a new method
    for filling the post-Pinatubo eruption data gap for 1991 to 1993 based on data from the Cryogenic Limb Array Etalon
    Spectrometer (CLAES). In addition, we developed a new method for populating high wintertime latitudes during the
    SAGE period employing a latitude-equivalent latitude conversion process that greatly improves the depiction of
    aerosol at high latitudes compared to earlier similar efforts. We report data in the troposphere only when and where
    it is available. This is primarily during the SAGE II period except during the most enhanced part of the Pinatubo
    period. It is likely that the upper troposphere during Pinatubo was greatly enhanced over non-volcanic periods and
    that domain remains substantially under characterized. We note that aerosol levels during the OSIRIS/CALIPSO period
    in the lower stratosphere at mid and high latitudes is routinely higher than what we observed during the SAGE II
    period. While this period had nearly continuous low-level volcanic activity, it is possible that the enhancement in
    part reflects deficiencies in the data set. We also expended substantial effort to quality assess the data set and
    the product is by far the best we have produced.
</p>
